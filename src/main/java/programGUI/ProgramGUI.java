    private void jButton5ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton5ActionPerformed
        if (transaction.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "No items added to Cart.\nPlease select \"Buy a product\" to purchase items", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        for (int i = 0; i < transaction.countOfItems(); i++) {
            productList.qtyOnHandDegree(transaction.getProduct(i));
        }
        JOptionPane.showMessageDialog(rootPane, "Sucсesful buying!\n" + "Total cost is " + transaction.getTotalCost() + " $");
        transaction.clear();
    }//GEN-LAST:event_jButton5ActionPerformed

