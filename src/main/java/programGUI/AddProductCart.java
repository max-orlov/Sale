    public AddProductCart(ProductList productList, SaleTransaction saleTransaction) {
        this.productList = productList;
        this.transaction = saleTransaction;
        if (this.productList.isEmpty()) {
            JOptionPane.showMessageDialog(rootPane, "There are currently no products added in the stock.\n"
                    + "Please select Option #1 to add a products to the stock.", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (this.transaction.isFull()) {
            JOptionPane.showMessageDialog(rootPane, "The Cart is already full" , "Error", JOptionPane.ERROR_MESSAGE);
        }
        initComponents();
        DefaultListModel<String> m = new DefaultListModel<String>();
        String[] s = transaction.getStringProducts();
        for (int i = 0; i < s.length; i++) {
            m.addElement(s[i]);
        }
        jList1.setModel(m);
    }


