    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        if (this.transaction.isFull()) {
            JOptionPane.showMessageDialog(rootPane, "The Cart is already full" , "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int b = jComboBox1.getSelectedIndex();
        //System.err.println(this.transaction.totalQty(productList.getProduct(b)));
        if (this.productList.getProduct(b) == null || this.productList.getProduct(b).getQtyOnHand() <
                this.transaction.totalQty(productList.getProduct(b)) +
                        productList.getProduct(b).getMinOrderQty()) {
            JOptionPane.showMessageDialog(rootPane, "This product can't be purchased", "Error", JOptionPane.ERROR_MESSAGE );
            return;
        }
        Product x = productList.getProduct(b);
        transaction.addPurchased(x);
        JOptionPane.showMessageDialog(rootPane, "Successful");
        DefaultListModel<String> m = (DefaultListModel<String>) jList1.getModel();
        m.addElement(x.toString());
        System.err.println(transaction);
    }//GEN-LAST:event_jButton1ActionPerformed

